public class VariableTypes {
    public static void main(String[] args) {
        String name = "Cleopatra";
        // int stands for integer and must be whole numbers
        int age = 2016 + 69;
        // Double can store up to 15 decimal digits. Float can only store 6-7
        double price = 1999.99;
        boolean truth = true;
        boolean falseness = !truth;
    }
}
